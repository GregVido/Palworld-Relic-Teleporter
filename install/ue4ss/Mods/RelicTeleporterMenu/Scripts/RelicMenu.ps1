# The DataTable contains the full list. The DataView applies live search filters.
$dataTable = $null
$dataView = $null

# Escape special characters used by DataView.RowFilter expressions.
function Escape-RowFilterValue([string]$value) {
    return $value.Replace("'", "''").
        Replace("[", "[[]").
        Replace("%", "[%]").
        Replace("*", "[*]")
}

function Load-RelicData {
    if (-not (Test-Path -LiteralPath $DataFile)) {
        $statusLabel.Text = "The relic data file could not be found."
        return
    }

    try {
        $items = Import-Csv -LiteralPath $DataFile -Delimiter "`t"

        $script:dataTable = New-Object System.Data.DataTable
        [void]$script:dataTable.Columns.Add("Index", [int])
        [void]$script:dataTable.Columns.Add("Relic", [string])
        [void]$script:dataTable.Columns.Add("ActorType", [string])
        [void]$script:dataTable.Columns.Add("X", [double])
        [void]$script:dataTable.Columns.Add("Y", [double])
        [void]$script:dataTable.Columns.Add("Z", [double])

        foreach ($item in $items) {
            $row = $script:dataTable.NewRow()
            $row["Index"] = [int]$item.Index
            $row["Relic"] = [string]$item.Relic
            $row["ActorType"] = [string]$item.ActorType
            $row["X"] = [double]::Parse(
                $item.X,
                [Globalization.CultureInfo]::InvariantCulture
            )
            $row["Y"] = [double]::Parse(
                $item.Y,
                [Globalization.CultureInfo]::InvariantCulture
            )
            $row["Z"] = [double]::Parse(
                $item.Z,
                [Globalization.CultureInfo]::InvariantCulture
            )
            [void]$script:dataTable.Rows.Add($row)
        }

        $script:dataView = New-Object System.Data.DataView($script:dataTable)
        $grid.DataSource = $script:dataView

        # Tune relative column widths for readability.
        if ($grid.Columns["Index"]) {
            $grid.Columns["Index"].FillWeight = 35
        }

        if ($grid.Columns["Relic"]) {
            $grid.Columns["Relic"].HeaderText = "Relic"
            $grid.Columns["Relic"].FillWeight = 100
        }

        if ($grid.Columns["ActorType"]) {
            $grid.Columns["ActorType"].HeaderText = "Actor type"
            $grid.Columns["ActorType"].FillWeight = 180
        }

        foreach ($coordinate in @("X", "Y", "Z")) {
            if ($grid.Columns[$coordinate]) {
                $grid.Columns[$coordinate].FillWeight = 70
                $grid.Columns[$coordinate].DefaultCellStyle.Format = "0.000"
            }
        }

        $searchBox.Text = ""
        $countLabel.Text = "$($script:dataTable.Rows.Count) relics"
        $statusLabel.Text = "List refreshed."
    }
    catch {
        $statusLabel.Text = "Could not read the relic list: $($_.Exception.Message)"
    }
}

function Apply-Filter {
    if ($null -eq $script:dataView) {
        return
    }

    $query = $searchBox.Text.Trim()

    if ([string]::IsNullOrWhiteSpace($query)) {
        $script:dataView.RowFilter = ""
    }
    else {
        $escaped = Escape-RowFilterValue $query
        $script:dataView.RowFilter =
            "Relic LIKE '%$escaped%' OR " +
            "ActorType LIKE '%$escaped%' OR " +
            "Convert([Index], 'System.String') LIKE '%$escaped%'"
    }

    $countLabel.Text =
        "$($script:dataView.Count) / $($script:dataTable.Rows.Count) relics"
}

# Return input focus to the game after the popup sends a teleport command.
function Focus-Palworld {
    Start-Sleep -Milliseconds 120

    $processNames = @(
        "Palworld-Win64-Shipping",
        "Palworld-WinGDK-Shipping",
        "Palworld"
    )

    foreach ($processName in $processNames) {
        $process = Get-Process -Name $processName -ErrorAction SilentlyContinue |
            Where-Object { $_.MainWindowHandle -ne 0 } |
            Select-Object -First 1

        if ($null -ne $process) {
            # 9 = SW_RESTORE
            [void][NativeWindowTools]::ShowWindowAsync(
                $process.MainWindowHandle,
                9
            )
            [void][NativeWindowTools]::SetForegroundWindow(
                $process.MainWindowHandle
            )
            return
        }
    }
}

# Write the selected row index for the Lua script to consume.
function Teleport-SelectedRelic {
    if ($grid.SelectedRows.Count -lt 1) {
        $statusLabel.Text = "Select a relic first."
        return
    }

    $selectedItem = $grid.SelectedRows[0].DataBoundItem

    if ($null -eq $selectedItem) {
        return
    }

    $index = [int]$selectedItem.Row["Index"]
    [System.IO.File]::WriteAllText($CommandFile, [string]$index)

    $statusLabel.Text = "Teleporting to relic #$index..."
    Focus-Palworld
}

