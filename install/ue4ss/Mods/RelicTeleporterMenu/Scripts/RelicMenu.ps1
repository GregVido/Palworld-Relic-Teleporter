<#
    Palworld Relic Teleporter
    Windows Forms popup

    This script displays the relic list generated by main.lua. It does not
    modify Palworld directly. When the user selects a relic, the script writes
    its numeric index to a command file. The UE4SS Lua script reads that index
    and performs the teleportation on the Unreal Engine game thread.
#>

