# Poll for the SHOW command sent by a second PowerShell process when F8 is
# pressed while the popup is already running.
$uiTimer = New-Object System.Windows.Forms.Timer
$uiTimer.Interval = 200
$uiTimer.Add_Tick({
    if (-not (Test-Path -LiteralPath $UiCommandFile)) {
        return
    }

    try {
        $uiCommand = [System.IO.File]::ReadAllText($UiCommandFile).Trim()

        if ($uiCommand -eq "SHOW") {
            [System.IO.File]::WriteAllText($UiCommandFile, "")
            Load-RelicData

            if (
                $form.WindowState -eq
                [System.Windows.Forms.FormWindowState]::Minimized
            ) {
                $form.WindowState =
                    [System.Windows.Forms.FormWindowState]::Normal
            }

            $form.Show()
            $form.TopMost = $true
            $form.Activate()
            $form.BringToFront()
        }
    }
    catch {
        # Ignore temporary read/write races between the two processes.
    }
})
$uiTimer.Start()

# Clean up runtime files when the popup closes.
$form.Add_FormClosed({
    $uiTimer.Stop()
    Remove-Item -LiteralPath $LockFile -Force -ErrorAction SilentlyContinue
    Remove-Item -LiteralPath $UiCommandFile -Force -ErrorAction SilentlyContinue
})

