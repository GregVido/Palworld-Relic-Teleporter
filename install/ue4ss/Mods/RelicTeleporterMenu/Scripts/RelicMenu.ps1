function Test-ExistingMenu {
    if (-not (Test-Path -LiteralPath $LockFile)) {
        return $false
    }

    try {
        $existingPidText = [System.IO.File]::ReadAllText($LockFile).Trim()
        $existingPid = 0

        if ([int]::TryParse($existingPidText, [ref]$existingPid)) {
            $existingProcess = Get-Process -Id $existingPid -ErrorAction SilentlyContinue

            if ($null -ne $existingProcess) {
                # Ask the already running popup to reload its data and return
                # to the foreground, then terminate this duplicate process.
                [System.IO.File]::WriteAllText($UiCommandFile, "SHOW")
                return $true
            }
        }
    }
    catch {
        # A malformed or inaccessible lock file is treated as stale.
    }

    Remove-Item -LiteralPath $LockFile -Force -ErrorAction SilentlyContinue
    return $false
}

if (Test-ExistingMenu) {
    exit
}

